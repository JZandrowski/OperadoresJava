public class OperadoresAritmeticos {
	
	public static void main(String[] args) {
		
		int a=4;
		int b=6;
		int c=3;
		
		System.out.println(a+b);
		System.out.println(a+c*b);
		System.out.println(c*c);
		System.out.println(b/c);
		System.out.println(b%c);
		System.out.println((a+b)*(a-b));
		
	}
}
